package com.java.backend;

public class Employ {

	String name;
	int salary;
	int id;
	
	//Default constructor
	public Employ() {
		
	}
	
	
	//parameterized constructor
	public Employ(String name,int salary,int id) {
		this.name=name;
		this.salary=salary;
		this.id=id;
	}
	
}
